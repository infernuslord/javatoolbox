package toolbox.util.ui.layout;

/**
 * Defines commonly used constants for directions and positions.
 */
public interface Direction
{
    /** Center */
    int CENTER = 0;
    
    /** North */
    int NORTH = 1;
    
    /** North east */
    int NORTHEAST = 2;
    
    /** East */
    int EAST = 3;
    
    /** South east */
    int SOUTHEAST = 4;
    
    /** South */
    int SOUTH = 5;
     
    /** South west */
    int SOUTHWEST = 6;
    
    /** West */
    int WEST = 7;
    
    /** North west */
    int NORTHWEST = 8;

    /** Left */
    int LEFT = WEST;
    
    /** Right */
    int RIGHT = EAST;
    
    /** Top */
    int TOP = NORTH;
    
    /** Bottom */
    int BOTTOM = SOUTH;
    
    /** Up */
    int UP = NORTH;
    
    /** Down */
    int DOWN = SOUTH;
}